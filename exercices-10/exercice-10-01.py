# exo 10.1
# Créer une fonction nommée `my_sum()` qui :
# - prend deux paramètres
# - additionne ces deux paramètres
# - renvoie le résultat
# Appelez la fonction et affichez le résultat

# réponse 10.1

def my_sum(a, b):
    result = a + b
    return result

# Appel de la fonction et affichage du résultat
print(my_sum(3, 4))
